# Data types define the type of data stored in a variable.
# In python, the data type for a variable is declared depending on the type of data that is stored when the variable is first time used.
# The most important data types in python are int, float, bool for single values and list, str, tuple, sets, dictionary for sequence values.

# Demonstartion for single values:
var_int = 5
print(type(var_int))
var_float = 5.5
print(type(var_float))
var_bool = True
print(type(var_bool))
# Demonstration for sequence values:
list_type = [1, 2, 3, 4, 5]
print(type(list_type))
str_type = 'Hello'
print(type(str_type))
set_type = {1, 2, 3, 4, 5, 5}
print(type(set_type))
tuple_type = (1, 2, 3, 4)
print(type(tuple_type))
dictionary_type = {'a': 1, 'b': 2}
print(type(dictionary_type))