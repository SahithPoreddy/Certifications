# Strings are collections/sequences of characters. 
# Just like in any sequence, the characters in the string can be accessed through their index numbers.
# Note: Python follows '0' based indexing. Meaning '0' indicates the first element in the string.
# 'len()' method is used to find the length of a sequence.
# A string can be declared using either single quotes or double quotes.

# Declaration of a string
type_str = 'Hello'
print(type_str)

# Length of a string
print(len(type_str))

# Accessing individual element in a string
print(type_str[0])

# Concatenation of two strings
str_a = 'Hello'
str_b = 'There'
print(str_a + str_b)