# Code to print a statement
print('Hello, World !')

# Declaring variables
# 1. Integer
var_int = 5
print(var_int)
# 2. Character
var_char = 'a'
print(var_char)
# 3. String
var_string = 'Hello'
print(var_string)

# Deleting an already declared variable
var_sample = 5
del var_sample
# Outputs and error message saying that 'var_sample' is not defined
print(var_sample)

# Input from the user
name = input()
print("Hello " + name)