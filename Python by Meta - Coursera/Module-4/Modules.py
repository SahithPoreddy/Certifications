# A module is a collection of already defined methods to make the code more re-usable.
# Module focuses on scope, reusability and simplicity of the code.
# In python, any file with the extension '.py' can be a module.
# Modules can be imported and used using the 'import <module_name>' statement.
# Demonstration:
import math

print(int(math.pow(2, 3)))
